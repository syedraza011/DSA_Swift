**Arrays:**

1. **Find Maximum and Minimum:** Write a function to find the maximum and minimum elements in an array of integers.

2. **Even and Odd Sum:** Create a program that calculates the sum of even and odd numbers separately in an array of integers.

3. **Array Reversal:** Write a function to reverse an array of elements. You should do this in-place, without using any additional memory.

4. **Find Common Elements:** Write a function that finds common elements between two arrays.

5. **Array Rotation:** Implement a function to rotate an array to the right by a given number of positions.

6. **Remove Duplicates:** Write a program that removes duplicates from an array and returns a new array with unique elements.

7. **Array Sorting:** Implement sorting algorithms (e.g., Bubble Sort, Merge Sort) to sort an array of integers.

These exercises cover a range of concepts related to strings and arrays, such as manipulation, comparison, counting, reversing, and more. They'll help you develop a better understanding of these data structures and build your problem-solving skills in Swift.
